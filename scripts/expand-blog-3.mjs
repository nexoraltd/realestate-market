import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const blogDir = join(__dirname, '../content/blog');

const appendContent = (filename, addition) => {
  const filePath = join(blogDir, filename);
  let content = readFileSync(filePath, 'utf8');
  const marker = '<PlanCTA />';
  const idx = content.lastIndexOf(marker);
  if (idx !== -1) {
    content = content.slice(0, idx) + addition + '\n' + content.slice(idx);
  } else {
    content = content.trimEnd() + '\n\n' + addition;
  }
  writeFileSync(filePath, content, 'utf8');
  return content.replace(/^---[\s\S]*?---/, '').trim().length;
};

