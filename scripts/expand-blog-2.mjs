const appendContent = (filePath, addition) => {
  let content = readFileSync(filePath, 'utf8');
  // Insert before <PlanCTA /> if exists, otherwise append
  const marker = '<PlanCTA />';
  const idx = content.lastIndexOf(marker);
  if (idx !== -1) {
    content = content.slice(0, idx) + addition + '\n' + content.slice(idx);
  } else {
    content = content.trimEnd() + '\n\n' + addition;
  }
  writeFileSync(filePath, content, 'utf8');
  const body = content.replace(/^---[\s\S]*?---/, '').trim();
  return body.length;
};

